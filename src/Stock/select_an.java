    private void fillCombo1_an() {
        String sql = "SELECT SCHEMA_NAME FROM SCHEMATA where schema_name like '2%' order by schema_name desc";
        try {
            //jComboBox1_an.addItem("          ");            
            Rs = St.executeQuery(sql);
            while (Rs.next()) {
                String nme = Rs.getString("schema_name").substring(0,4);
                jComboBox1_an.addItem(nme);
            }            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur dans la liste jComboBox1\n" + e.getMessage());
        }
    }
    
    
